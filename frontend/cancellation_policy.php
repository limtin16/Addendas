<h1>Política de Cancelación y Reembolsos</h1>

<p>
En AddendaFácil, ofrecemos créditos digitales para la generación de addendas CFDI 4.0.
</p>

<h2>❌ Cancelaciones</h2>
<p>
Debido a la naturaleza digital del servicio, una vez confirmado el pago y asignados los créditos
a la cuenta del usuario, no es posible cancelar la compra.
</p>

<h2>💰 Reembolsos</h2>
<p>
No se realizan reembolsos una vez que los créditos han sido acreditados en la cuenta del usuario.
</p>

<p>
En caso de errores técnicos atribuibles a la plataforma (por ejemplo, compra duplicada o cargos incorrectos),
el usuario podrá solicitar una revisión enviando evidencia al correo de soporte.
</p>

<h2>📅 Vigencia de créditos</h2>
<p>
Los créditos adquiridos tienen una vigencia específica dependiendo del paquete comprado.
Una vez vencidos, los créditos no podrán ser utilizados ni reembolsados.
</p>

